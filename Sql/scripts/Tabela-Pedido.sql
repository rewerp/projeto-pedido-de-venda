CREATE TABLE PEDIDO (
    NUMERO_PEDIDO INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATA_EMISSAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP(),
    CODIGO_CLIENTE INTEGER NOT NULL,
    VALOR_TOTAL NUMERIC(15,2) DEFAULT 0,
    CONSTRAINT FK_PEDIDO_CLIENTE FOREIGN KEY (CODIGO_CLIENTE) REFERENCES CLIENTE(CODIGO)
);

CREATE INDEX IDX_PEDIDO_CODIGO_CLIENTE ON PEDIDO (CODIGO_CLIENTE);